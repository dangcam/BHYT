

namespace BHYT.VO
{
    public class BenhVO
    {
        private string maBenh;
        private string tenBenh;
        private string maLoai;
        private string maNhom;

        public string MaBenh
        {
            get
            {
                return maBenh;
            }

            set
            {
                maBenh = value;
            }
        }

        public string TenBenh
        {
            get
            {
                return tenBenh;
            }

            set
            {
                tenBenh = value;
            }
        }

        public string MaLoai
        {
            get
            {
                return maLoai;
            }

            set
            {
                maLoai = value;
            }
        }

        public string MaNhom
        {
            get
            {
                return maNhom;
            }

            set
            {
                maNhom = value;
            }
        }
    }
}
